export function normalizeLanguage(language?: string | null): LanguageCode {
  if (language === "de" || language === "fr" || language === "es") return language;
  return "en";
}

export function t(
  language: string | undefined,
  key: TranslationKey,
  values: Record<string, string | number> = {},
): string {
  const template = TRANSLATIONS[normalizeLanguage(language)][key] ?? EN[key];
  return template.replace(/\{(\w+)\}/g, (_, token: string) =>
    Object.prototype.hasOwnProperty.call(values, token) ? String(values[token]) : `{${token}}`,
  );
}
